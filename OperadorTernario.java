package javabasico;

public class OperadorTernario {

	public static void main(String[] args) {
		
		int a = 10, b = 14;
		
		// operador ternário
		int resultado = (a > b)? a:b;
		
		System.out.println(resultado);
		
		System.out.printf("A raiz quadrada de 255 é: %.2f", java.lang.Math.sqrt(255));
		
	}
}